package U1W1D2;

public class Ex2 {
    public static void stampaLettere( int num){
        switch (num){
            case 0:
                System.out.println("zero");
                break;
            case 1:
                System.out.println("one");
                break;
            case 2:
                System.out.println("two");
                break;
            case 3:
                System.out.println("three");
                break;
            default:
                System.out.println("Error");
        }
    }
}
